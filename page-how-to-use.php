        <h3>APPLICATION:</h3>
        <p>Spread powder over a spill on water or a hard surface until excess white polymer remains on the surface. Apply by hand or use a hand spreader, blower, or other conventional methods for spreading out the powder. You will notice the powder starts to work upon contact.</p>

